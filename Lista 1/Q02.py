#2 - Faça um programa que receba dois números. Mostre a soma, subtração, multiplicação e a divisão dos números lidos.#

num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2

print("A soma dos números é:", soma)
print("A subtração dos números é:", subtracao)
print("A multiplicação dos números é:", multiplicacao)
print("A divisão dos números é:", divisao)
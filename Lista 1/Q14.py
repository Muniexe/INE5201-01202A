#14 - Leia dos números como entrada. Utilizar os operadores relacionais (==, !=, <, >, <=, >=) para comparar os números informados. #
n1 = float(input("Digite o primeiro número: "))
n2 = float(input("Digite o segundo número: "))
print("n1 == n2:", n1 == n2)
print("n1 != n2:", n1 != n2)
print("n1 < n2:", n1 < n2)
print("n1 > n2:", n1 > n2)
print("n1 <= n2:", n1 <= n2)
print("n1 >= n2:", n1 >= n2)
